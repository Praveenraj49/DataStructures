public class Cake {
    int weight;
    int value;

    public Cake(int weight, int value)
    {
        this.weight = weight;
        this.value =  value;
    }

    public int getWeight()
    {
        return  weight;
    }
    public int getValue()
    {
        return value;
    }
}
